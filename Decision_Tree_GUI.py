    def browseBtnClicked(self, text):
        if text == 'train':
            filename = QFileDialog.getOpenFileName()
            self.trainingPathTextbox.setText(filename[0])
        elif text == 'valid':
            if self.model is None:
                msg = QMessageBox()
                msg.setIcon(QMessageBox.Critical)
                msg.setWindowTitle('Failed')
                msg.setText('Please train the model first.')
                msg.exec_()
                return
            filename = QFileDialog.getOpenFileName()
            self.validPathTextbox.setText(filename[0])
            val_dataset = pd.read_csv(self.validPathTextbox.text())
            self.Xval = val_dataset.iloc[:, :-2].values.astype(np.int8)
            self.Yval = np.array(val_dataset.iloc[:, -1].astype('category').cat.codes, dtype= np.int8)
        elif text == 'test':
            filename = QFileDialog.getOpenFileName()
            self.testPathTextbox.setText(filename[0])
    
