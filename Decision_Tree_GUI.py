    def trainModel(self):
        self.trainPath = self.trainingPathTextbox.text()
        if self.trainPath == '':
            msg = QMessageBox()
            msg.setIcon(QMessageBox.Critical)
            msg.setWindowTitle('Failed')
            msg.setText('Please check the path of the training data csv file.')
            msg.exec_()
            return
        
        self.train_dataset = pd.read_csv(self.trainPath)
        self.Xtrain = self.train_dataset.iloc[:, :-2].values.astype(np.int8)
        self.Ytrain = np.array(self.train_dataset.iloc[:, -1].astype('category').cat.codes, dtype= np.int8)
        
        if self.maxDepthTextbox.text() == '':
            self.model = DecisionTree()
        else:
            self.model = DecisionTree(int(self.maxDepthTextbox.text()))
        
        QApplication.setOverrideCursor(QtCore.Qt.WaitCursor)
        self.model.fit(self.Xtrain, self.Ytrain)
        QApplication.restoreOverrideCursor()
        msg = QMessageBox()
        msg.setIcon(QMessageBox.Information)
        msg.setWindowTitle('Successful')
        msg.setText('The model has been trained successfully!')
        msg.exec_()
        return
    
